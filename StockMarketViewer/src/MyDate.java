class MyDate {
    // ********************************************************************** //
    // ** Instant Field
    // ********************************************************************** //
    private String year;
    private String month;
    private String day;
    private String date;

    // ********************************************************************** //
    // ** Constructor
    // ********************************************************************** //
    /** Input format: MM/dd/yy */
    MyDate(String s) {
        this.date = s;
        String[] sp = s.split("[/]+");
        this.month = sp[0];
        this.day = sp[1];
        this.year = sp[2];
    }

    // ********************************************************************** //
    // ** Getter
    // ********************************************************************** //
    String getYear() {
        return year;
    }

    String getMonth() {
        return month;
    }

    String getDay() {
        return day;
    }

    String getDate() {
        return date;
    }
}